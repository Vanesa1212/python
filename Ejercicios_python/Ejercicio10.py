# Diferencias entre Python y JavaScript:

# 1. Tipado:
#    - Python: usa tipado dinámico y fuerte, es decir, no necesitas declarar el tipo
#      de una variable, pero una vez que le das un valor, el tipo no cambia.
#    - JavaScript: también tiene tipado dinámico, pero es más flexible, lo que
#      significa que puede hacer conversiones de tipo automáticamente, lo cual a veces
#      puede causar errores inesperados.

# 2. Sintaxis:
#    - Python: se basa en la indentación para definir bloques de código. Esto hace que el
#      código sea más limpio y fácil de leer.
#    - JavaScript: utiliza llaves {} para definir bloques de código y aunque los puntos y
#      comas son opcionales, es una buena práctica usarlos para evitar problemas.

# 3. Orientación a objetos:
#    - Python: es multiparadigma, lo que significa que soporta programación orientada a objetos,
#      funcional y procedural.
#    - JavaScript: también es multiparadigma, pero su orientación a objetos se basa en prototipos
#      en lugar de clases, aunque las clases se pueden usar desde ES6.

# 4. Entornos de ejecución:
#    - Python: se usa mucho en el backend, ciencia de datos, inteligencia artificial y scripts
#      de automatización.
#    - JavaScript: es el rey del desarrollo web en el frontend, pero con Node.js también se puede
#      usar en el backend.

# 5. Librerías y Frameworks:
#    - Python: tiene muchas librerías y frameworks para distintas tareas, como Django y Flask para
#      desarrollo web, y TensorFlow y PyTorch para aprendizaje automático.
#    - JavaScript: también tiene una gran cantidad de herramientas, como React, Angular y Vue.js para
#      el frontend, y Express para el backend.

# 6. Manejo de asincronía:
#    - Python: utiliza asyncio y palabras clave como async y await para trabajar con código asíncrono.
#    - JavaScript: tiene un soporte asíncrono nativo muy potente con callbacks, promesas y async/await.

# 7. Comunidad y Uso:
#    - Python: es muy popular en el mundo académico y científico, y es conocido por ser fácil de aprender
#      y usar.
#    - JavaScript: es indispensable para el desarrollo web y tiene una comunidad enorme de desarrolladores,
#      dado que prácticamente todos los navegadores web lo usan.

# 8.Mutabilidad:

#    -Python tiene tipos de datos mutables e inmutables como set (mutable) y list (Inmutable).
#    -JavaScript no tiene el concepto de mutable e inmutable.


#Strings

#    -En Python, el código fuente es ASCII por defecto a menos que especifiquemos algún formato de codificación.
#    -JavaScript debe codificarse como UTF-16 y no hay soporte incorporado para manipular bytes.